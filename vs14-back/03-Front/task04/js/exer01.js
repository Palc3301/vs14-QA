/*Você foi contratado para criar um programa em JavaScript que converta uma temperatura fornecida em graus Celsius para graus Fahrenheit. Utilize as fórmulas de conversão apropriadas para realizar essa tarefa. O programa deve declarar uma constante (celsiusTemperature) com o valor da temperatura em graus Celsius e, em seguida, calcular e exibir a correspondente temperatura em graus Fahrenheit.
*/
const celsiusTemperature = 25;
const fahrenheitTemperature = (celsiusTemperature * 9/5) + 32;
console.log(`A temperatura de ${celsiusTemperature}°C corresponde a ${fahrenheitTemperature}°F.`);
